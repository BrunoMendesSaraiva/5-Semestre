package revendas.entities;

public class custosAssociados {
    private int id;
    private int funilaria;
    private int pintura;
    private int eletricista;
    private int combustivel;
    private int transporte;
    private int despesLoc;
    private int despesImp;
    private int manutencao;
    
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getFunilaria() {
        return funilaria;
    }
    public void setFunilaria(int funilaria) {
        this.funilaria = funilaria;
    }
    public int getPintura() {
        return pintura;
    }
    public void setPintura(int pintura) {
        this.pintura = pintura;
    }
    public int getEletricista() {
        return eletricista;
    }
    public void setEletricista(int eletricista) {
        this.eletricista = eletricista;
    }
    public int getCombustivel() {
        return combustivel;
    }
    public void setCombustivel(int combustivel) {
        this.combustivel = combustivel;
    }
    public int getTransporte() {
        return transporte;
    }
    public void setTransporte(int transporte) {
        this.transporte = transporte;
    }
    public int getDespesLoc() {
        return despesLoc;
    }
    public void setDespesLoc(int despesLoc) {
        this.despesLoc = despesLoc;
    }
    public int getDespesImp() {
        return despesImp;
    }
    public void setDespesImp(int despesImp) {
        this.despesImp = despesImp;
    }
    public int getManutencao() {
        return manutencao;
    }
    public void setManutencao(int manutencao) {
        this.manutencao = manutencao;
    }

}
